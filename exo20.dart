String concatener(String chaine1, String chaine2) {
  return chaine1 + chaine2;
}

void main() {
  // Exemple d'utilisation de la fonction concatener
  String resultat = concatener("Bonjour, ", "Classe!");

  // Affichage du résultat de la concaténation
  print("Résultat de la concaténation : $resultat");
}
