void main() {
  // Boucle for qui affiche les nombres de 1 à 10
  for (int i = 1; i <= 10; i++) {
    print(i);
  }

  // Ligne vide pour séparer les sorties des deux boucles
  print('---');

  // Boucle while qui affiche les nombres de 10 à 1
  int j = 10;
  while (j >= 1) {
    print(j);
    j--;
  }
}//Dans ce code :
//La boucle for (int i = 1; i <= 10; i++) initialise i à 1 et continue d'exécuter le bloc 
//de code tant que i est inférieur ou égal à 10, en incrémentant i de 1 à chaque itération.
//La boucle while (j >= 1) initialise j à 10 et continue d'exécuter le bloc de code tant que j 
//est supérieur ou égal à 1, en décrémentant j de 1 à chaque itération.