void main() {
  // Définir un nombre pour vérifier
  int nombre = -5; // Vous pouvez changer cette valeur pour tester d'autres cas

  // Vérifier si le nombre est positif, négatif ou zéro
  if (nombre > 0) {
    print('Le nombre $nombre est positif.');
  } else if (nombre < 0) {
    print('Le nombre $nombre est négatif.');
  } else {
    print('Le nombre est zéro.');
  }
}

