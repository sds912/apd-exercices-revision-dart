void main() {
  // Déclaration d'une variable nomComplet comme final et assignation de votre nom complet
  final String nomComplet = "AidaDiatta";

  // Déclaration d'une constante vitesseLumiere avec la valeur 299792458 (m/s)
  const int vitesseLumiere = 299792458; // en mètres par seconde

  // Affichage des valeurs
  print('Nom complet: $nomComplet');
  print('Vitesse de la lumière: $vitesseLumiere m/s');
}

